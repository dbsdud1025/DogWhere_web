
public class dogDTO {
	private String dogName;
	private String dogBreed;
	private String dogSex;
	private String dogphotoUrl;
	private String dogstate;
	
	public String getDogName() {
		return dogName;
	}
	public void setDogName(String dogName) {
		this.dogName = dogName;
	}
	public String getDogBreed() {
		return dogBreed;
	}
	public void setDogBreed(String dogBreed) {
		this.dogBreed = dogBreed;
	}
	public String getDogSex() {
		return dogSex;
	}
	public void setDogSex(String dogSex) {
		this.dogSex = dogSex;
	}
	public String getDogphotoUrl() {
		return dogphotoUrl;
	}
	public void setDogphotoUrl(String dogphotoUrl) {
		this.dogphotoUrl = dogphotoUrl;
	}
	public String getDogstate() {
		return dogstate;
	}
	public void setDogstate(String dogstate) {
		this.dogstate = dogstate;
	}
}
